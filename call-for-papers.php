  <p>The 23<sup>rd</sup> GCA Organizing Group invites Expressions of Interest (EOI) for research papers to be presented during the conference. Actuaries as well as other professionals (including academicians) can submit an EOI.</p>
  <p style="
    font-weight: bold;
">A: Introduction</p>
  <p>The GCA organized annually since 1999 is a signature three-day event attracting stakeholders in the Insurance and financial sector, with an evening being devoted to Actuarial Gala Function and Awards (AGFA). The AGFA as well as GCA are organized in partnership with the Insurance and Pensions Industry, Consulting Organizations, and other stakeholders in the event, called Partners.</p>
  <p>The 23<sup>rd</sup> GCA will be held over 13 and 14 February 2024, where actuaries and non-actuaries assemble in a global ambience to share thoughts and debate matters that affect the financial services industry in general and insurance industry in particular. Glimpses from past GCA’s are also available at <a href="https://www.youtube.com/user/IAIMumbai/videos">https://www.youtube.com/user/IAIMumbai/videos</a></p>
  <p>The event starts on the evening of 12 February 2024 with Actuarial Gala Function and Awards (AGFA) followed by the 23<sup>rd</sup> GCA Welcome Reception and Dinner along with two days’ discussion and deliberation on various subjects of professional interest. It is a great opportunity to witness, partake and celebrate the recognition of young actuarial professionals and contribute research papers on any of the core disciplines identified in the call.</p>
  <p>The 23<sup>rd</sup> GCA’s theme is <strong>‘Data, Disruptions, and the Actuary’</strong></p>
  <p>The Papers should be from authors with deep knowledge and expertise. The Institute of Actuaries of India (IAI) is taking steps to publish a journal of peer reviewed research papers with indexing facility on digital and other academic platforms. In this backdrop, the 23<sup>rd</sup> GCA is likely to be a meaningful platform for academic contributions in addition to profession- and industry-led discussions.</p>
  <p>The 23<sup>rd</sup> GCA is expected to attract around 1,000 participants. The 22nd GCA held in 2023 had over 800 participants.</p>
