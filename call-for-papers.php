  <p style="
    font-weight: bold;
">Appendix III: Guidelines for paper submission.</p>
  <p><b>Order of paper</b></p>
  <p>Each paper contains: Title</p>
  <div style="margin-left:15px;">
    <p style="font-size: 13px;">Author(s)<br>name(s)<br>Abstract<br>Keywords<br>Correspondence details Introduction Text Acknowledgements<br>References and<br>Appendix(ces).</p>
  </div>
  <p style="
    font-weight: bold;
">Title</p>
  <p>The title of the paper has an initial capital only, e.g.: Indian actuarial profession</p>
  <p style="
    font-weight: bold;
">Author’s name(s)</p>
  <p>The author or authors’ name(s) are below the title with an asterisk denoting the corresponding author</p>
  <p>e.g., Vijaya Jeet* and Ayan Armaan.</p>
  <p style="
    font-weight: bold;
">Abstract</p>
  <p>It is a summary of the paper, in one long or two or three short paragraphs and not over 250 words. The abstract should not contain references.</p>
  <p style="
    font-weight: bold;
">Keywords</p>
  <p>Choose keywords to indicate matters of interest in the paper. Restrict to five words or short phrases, e.g.:</p>
  <p>Policyholder behaviour, Generator matrix, Absorbing state, Transition, Morbidity.</p>
  <p>Note that keywords should have an initial capital.</p>
  <p style="
    font-weight: bold;
">Correspondence details</p>
  <p>The name and contact address of the corresponding author with e-mail address must be given at the bottom of the first page as, e.g.:</p>
  <p><a href="mailto:%20name.familyname@companyname.in">*Correspondence to: Vijaya Jeet, Marketing Department, Steadfast Actuarial Consultancy, Address, E-mail:</a> <a href="mailto:%20name.familyname@companyname.in">&nbsp;name.familyname@companyname.in</a></p>
  <p style="
    font-weight: bold;
">Main text</p>
  <p>All sections are numbered, and each section (apart from the introduction) can be divided into numbered sub-sections (if required). Numbered paragraphs are not needed but encouraged.</p>
  <ol>
    <li>
      <p>Introduction</p>
    </li>
    <li>
      <p>Problem Definition</p>
      <ol>
        <li>
          <p>Problem Defined in detail</p>
        </li>
      </ol>
    </li>
    <li>
      <p>Approach to analysis</p>
    </li>
  </ol>
  <p><b>N.B.:</b> If using acronyms, the full name/ title must be given at the first mention followed by the initials in brackets e.g., Institute of Actuaries of India (IAI) which can then be subsequently referred to as IAI.</p>
  <p style="
    font-weight: bold;
">Formulae</p>
  <p>If formulae are numbered, do so consecutively after each formula with the number placed in</p>
  <p>brackets e.g. E(X) = ∫ x.f(x) dx … (1)</p>
  <p style="
    font-weight: bold;
">Acknowledgements</p>
  <p>Often authors like to include acknowledgements to those who have helped them in the development of their paper. These should appear at the end of the main text. e.g.: We are most grateful to the XYZ for their help and funding of this paper.</p>
  <p style="
    font-weight: bold;
">References</p>
  <p>References in the text should be in the form:</p>
  <p>Top &amp; Bottom (2001) or (see Top <i>et al</i>., 2001).</p>
  <p>If there are two authors ‘&amp;’ should be used between surnames, if more than two, only the first named author’s surname should be used followed by <i>et al</i>., see above example.</p>
  <p>Place brackets around the year if appearing in the text, if appearing within bracketed comments, a</p>
  <p>comma should follow the authors’ names before the year, see above examples.</p>
  <p style="
    font-weight: bold;
">List of references</p>
  <p>The full references must be given in this section and ALL references must be cross-referenced within the text. References are in alphabetical order by first author, and in date order for any one author. If URLs are given, the date of access must be shown.</p>
  <p style="
    font-weight: bold;
">References</p>
  <p>Top, A.B., Low, Y. Z. (2001). <i>The Indian Statistical Journal.</i> <a href="http://www.indianstatisticaljournal.com/blog">Clear Publishers. Accessed at</a> www.indianstatisticaljournal.com/blog accessed on 14 August 2023.</p>
  <p>All authors’ names must be shown, with surname first, followed by initials, &amp; before the last name,</p>
  <p>year in brackets followed by a full stop.</p>
  <p><b>Journal references</b> – full title of paper, followed by journal name in full and in italics. Volume number follows in bold, followed by page numbers (not in bold).</p>
  <p>Modern, A.B., Traditional, Y. Z. (2022). The Distribution Conundrum. <i>Asian Insurance Journal,</i> <strong>20</strong>, 81-149.</p>
  <p><strong>Book references –</strong> book title in italics, followed by edition (where given), formatted as e.g. 4th ed. Publisher name next, followed by place, formatted as e.g. Wiley, Winchester.</p>
  <p>If the same author(s) and year of publication are referred to more than once they must be distinguished by a, b etc. e.g.</p>
  <p>Brown (2008a, b) in text and in references list as</p>
  <p>Brown, B. (2008a). Pensions crisis. <i>Annals of Actuarial Science</i>, <strong>2</strong>, 21-</p>
  <p>46. Brown, B. (2008b). <i>Pensions</i>. Cambridge University Press.</p>
  <p>If URLs are used the access date must be included e.g.</p>
  <p><a href="http://www.coso.org/.../ERM/COSO_ERM_ExecutiveSummary.pdf">COSO (2004). Enterprise Risk Management - Integrated Framework.</a> www.coso.org/.../ERM/COSO_ERM_ExecutiveSummary.pdf [accessed December 2009].</p>
  <p>If a paper was presented some time ago, please check if it has subsequently been published in a journal, if so the journal reference needs to be given.</p>
  <p><a href="http://libweb.anglia.ac.uk/referencing/harvard.htm">See</a> http://libweb.anglia.ac.uk/referencing/harvard.htm for further information on referencing.</p>
  <p style="
    font-weight: bold;
">Appendix(ces)</p>
  <p>Appendices must follow the references.</p>
  <p>Figures, tables and illustrations</p>
  <p>When using figures, tables and illustrations throughout the paper, please follow these guidelines.</p>
  <p style="
    font-weight: bold;
">General guidelines</p>
  <ol>
    <li>
      <p>All figures and tables should be numbered throughout the paper, and should have a heading or caption.</p>
    </li>
    <li>
      <p>Please use explicit references to the figures and tables in the text (e.g., ‘see table 4’). Avoid words like ‘above’ and ’below’, as after typesetting the figures and tables might not appear as they do in the manuscript.</p>
    </li>
    <li>
      <p>All figures should be supplied as separate files. Please do not embed the figures in the text.</p>
    </li>
    <li>
      <p>Please ensure that the material you submit is of the best possible quality e.g., avoid using tinted panels or surrounding borders, the range of tints in grey scale illustrations should be above 15%.</p>
    </li>
    <li>
      <p>Maths labels should be typed exactly as they appear in the text. For example, if a symbol appears in italic in the text or equation, it should also appear in italic in the figure. All fonts should be embedded.</p>
    </li>
    <li>
      <p>Common packages such as Adobe Illustrator have options which allow you to output to TIFF or EPS, and we would prefer it if you could supply your files in this format.</p>
    </li>
    <li>
      <p>Submit figures sized to fit the actual column or page width of the journal so that reduction or enlargement is not necessary. Note that some software packages are generally NOT suitable for conversion to print reproduction, and these include:</p>
      <ul>
        <li>
          <p>Microsoft PowerPoint files</p>
        </li>
        <li>
          <p>Images created in Microsoft Word</p>
        </li>
        <li>
          <p>GIFF images downloaded from the web</p>
        </li>
      </ul>
    </li>
  </ol>
