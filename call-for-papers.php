    </li>
    <li>
      <p><a href="http://115.112.202.200/callforpapers.aspx">EOI must be submitted in English, via the</a> Online Submission System only. E-mail submission will NOT be accepted.</p>
    </li>
    <li>
      <p>EOI will be selected based on quality, author credentials, and appropriateness to the conference theme.</p>
    </li>
    <li>
      <p>All the speakers selected/ through the would be provided free registration for all the conference days.</p>
